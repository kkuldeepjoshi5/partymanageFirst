package com.eventManagment.Entity;

public class Requirement {

	private int requirementId;
	private String requirementName;
	private int requirementStatus;
	private String requirementDesc;
	public int getRequirementId() {
		return requirementId;
	}
	public void setRequirementId(int requirementId) {
		this.requirementId = requirementId;
	}
	public String getRequirementName() {
		return requirementName;
	}
	public void setRequirementName(String requirementName) {
		this.requirementName = requirementName;
	}
	public int getRequirementStatus() {
		return requirementStatus;
	}
	public void setRequirementStatus(int requirementStatus) {
		this.requirementStatus = requirementStatus;
	}
	public String getRequirementDesc() {
		return requirementDesc;
	}
	public void setRequirementDesc(String requirementDesc) {
		this.requirementDesc = requirementDesc;
	}
	
}
